class d13_logger

{

	// ----------------------------------------------------------------------------------------
	//
	//
	// ----------------------------------------------------------------------------------------

	public

	function __construct()
	{
	}

	// ----------------------------------------------------------------------------------------
	//
	//
	// ----------------------------------------------------------------------------------------

	public

	function log($note = "")
	{
		$fd = fopen("./logs/log.txt", "a");
		fwrite($fd, $note . "\n");
		fclose($fd);
	}
}

